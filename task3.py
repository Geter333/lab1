# Функція для побудови рисунку
def draw_pattern(n):
    # Формуємо кожен рядок
    for i in range(n, 0, -1):
        # Виводимо числа від i до 1
        for j in range(n, n - i, -1):
            print(j, end=" ")
        print()  # Переходимо на новий рядок після кожного ряду


# Основна частина програми
def main():
    try:
        # Введення значення N
        n = int(input("Введіть число N (1 < N < 9): "))

        # Перевірка на правильний діапазон
        if n <= 1 or n >= 9:
            print("Помилка: N повинно бути більше 1 і менше 9.")
            return

        # Викликаємо функцію для побудови рисунку
        draw_pattern(n)

    except ValueError:
        print("Помилка: введіть ціле число.")


# Запуск основної функції
if __name__ == "__main__":
    main()
